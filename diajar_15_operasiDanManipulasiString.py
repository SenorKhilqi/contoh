# merubah case dari string 

# merubah semua ke upper case

salam = "hai"
print("normal = "  + salam)
salam = salam.upper() #upper merubah semua kata menjadi kapital
print("upper = "  + salam)

# merubah semua ke lower case
contoh = " asdfghjkl AWERHOFIAEG"
print ("normal = "  + contoh)
contoh = contoh.lower()
print("lower = " + contoh)

